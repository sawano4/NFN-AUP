type Dictionary = Record<keyof typeof dictionaries.fr, string>;

const LanguageContext = createContext<{
  language: Language;
  setLanguage: (language: Language) => void;
  t: Dictionary;
  dir: 'ltr' | 'rtl';
}>({
  language: 'fr',
  setLanguage: () => undefined,
  t: dictionaries.fr as Dictionary,
  dir: 'ltr',
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>('fr');

  useEffect(() => {
    const stored = localStorage.getItem('nfn-source-language');
    if (stored === 'fr' || stored === 'ar') {
      setLanguageState(stored);
    }
  }, []);

  const setLanguage = (next: Language) => {
    setLanguageState(next);
    localStorage.setItem('nfn-source-language', next);
  };

  const value = useMemo(() => {
    const t = dictionaries[language] as Dictionary;
    return { language, setLanguage, t, dir: t.dir as 'ltr' | 'rtl' };
  }, [language]);

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = value.dir;
  }, [language, value.dir]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  return useContext(LanguageContext);
}
