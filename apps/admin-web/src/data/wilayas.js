export function getWilayaCenter(wilayaName) {
  if (!wilayaName) return [28.0, 2.5]
  const key = wilayaName.toLowerCase()
  const found = WILAYAS.find(w => w.name.toLowerCase().includes(key) || key.includes(w.name.toLowerCase()))
  return found ? [found.lat, found.lng] : [28.0, 2.5]
}
